int		ft_strcmp(const char *s1, const char *s2)
{
	int		i;
	int		ret;

	i = 0;
	while ((s1[i] && s2[i]) && (s1[i] == s2[i]))
		i++;
	if (!s2[i] && s1[i])
		ret = ((s1[i] - s2[i]) < 0) ? 1 : (s1[i] - s2[i]);
	else if (!s1[i] && s2[i])
		ret = ((s1[i] - s2[i]) > 0) ? -1 : (s1[i] - s2[i]);
	else
		ret = s1[i] - s2[i];
	return (ret);
}
